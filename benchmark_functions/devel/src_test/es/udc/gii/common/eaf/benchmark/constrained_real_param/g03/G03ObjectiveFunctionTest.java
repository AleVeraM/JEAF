import static org.junit.Assert.*;

/**
 *
 * @author pilar
 */
public class G03ObjectiveFunctionTest {

    public G03ObjectiveFunctionTest() {
    }


    /**
     * Test of evaluate method, of class G03ObjectiveFunction.
     */
    @Test
    public void testEvaluate() {
        System.out.println("evaluate");
        double[] values = CEC2006Test.loadBestKnownSolution(3);
        G03Test.normalize(values);
        G03ObjectiveFunction instance = new G03ObjectiveFunction();
        double expResult = CEC2006Test.loadBestKnownResult(3);
        double result = instance.evaluate(values);
        boolean cond = CEC2006Test.error(expResult, result) <= CEC2006Test.epsilon;
        assertTrue(cond);
    }

}